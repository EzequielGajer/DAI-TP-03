const suma = (num1, num2) => {
    return num1 + num2;
}

const resta = (num1, num2) => {
    return num1 - num2;
}

function multiplicar (num1, num2) {
    let PI = 3.14;
    return ((num1 * num2) * PI)
}

function dividir () {
    return num1 / num2; 
}

export {suma, resta, multiplicar, dividir};